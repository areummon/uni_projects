package mx.unam.ciencias.icc.proyecto2;

/**
 * Enumeracion para las opciones del ordenador lexicografico.
 */
public enum Opciones {

    /* Modo cuando se da la bandera para reversa pero sin identificador */
    REVERSA_SIN_IDENTIFICADOR,
    /* Modo cuando se da el identificador y la bandera para reversa */
    REVERSA_CON_IDENTIFICADOR,
    /* Modo cuando se da el identificador pero no la bandera para reversa */
    IDENTIFICADOR,
    /* Modo cuando solamente se dan los archivos a ordenar */
    IMPRIME;
}
